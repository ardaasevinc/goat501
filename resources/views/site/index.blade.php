                                            He graduated from <strong>Anadolu University’s Faculty of Business
                                                Administration</strong>, yet his cinematic vision was
                                            shaped by the landscapes and cultural heritage of the <strong>Eastern Black
                                                Sea region</strong> where he was born and raised.<br><br>

                                            His debut feature, <strong>Goat 501</strong>, is a docudrama that merges
                                            observational realism with poetic minimalism.<br><br>

                                            Shot over three years, the film follows a shepherd who exists outside the
                                            bounds of economy and social progress —
                                            living in a timeless loop, as part of the herd rather than above it.<br><br>

                                            <strong>Çervatoğlu’s cinema</strong> is rooted in simplicity, silence, and
                                            authenticity.<br><br>

                                            He is particularly drawn to lives that exist on the margins of modern
                                            structures —
                                            where the absence of money, ambition, or hierarchy allows for deeper human
                                            truths to emerge.<br><br>

